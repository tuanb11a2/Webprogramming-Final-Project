    <h1>About this project</h1>
    <p>Books have become friends of us for a long time. With books, we are opened up to a whole new world of knowledge and imagination. It shows us wonders of the universe, drives us back to history and future, and guidelines us to the destinations of our life</p>

    <p>However, since the pandemic is spreading over the globe, preventing us from getting to our favorite pages, books in libraries cannot help us with their greate role. On the bright side, the growth of Internet and online materials gives us a chance to continue our journeys with our silent and intelligent friends, with the help of modern devices and technologies</p>

    <p>Therefore, this webpage aim at providing a free online book reading platform with thousand of famous book with high quality, so that everyone can enjoy their loved book while staying at home - even for fighting with the world's disease, currently, or for further reading when the disaster has come to an end.</p>

    <p>This is the idea for our final project of the Web Programming course of semsester 2020-2, at Hanoi University of Science and Technology.</p>
